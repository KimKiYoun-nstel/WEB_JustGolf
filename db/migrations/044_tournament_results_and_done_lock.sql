create or replace function public.guard_done_tournament_row_update()
returns trigger
language plpgsql
as $$
begin
  if old.status = 'done' and (to_jsonb(new) is distinct from to_jsonb(old)) then
    raise exception '종료된 대회 정보는 더 이상 수정할 수 없습니다.';
  end if;

  return new;
end;
$$;

