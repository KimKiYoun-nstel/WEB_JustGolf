create or replace function public.assert_tournament_not_done(
  p_tournament_id bigint,
  p_context text default null
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_status text;
begin
  if p_tournament_id is null then
    return;
  end if;

  select t.status
    into v_status
  from public.tournaments t
  where t.id = p_tournament_id;

  if v_status = 'done' then
    raise exception '%', coalesce(p_context, '종료된 대회는 수정할 수 없습니다.');
  end if;
end;
$$;

revoke all on function public.assert_tournament_not_done(bigint, text) from public;
grant execute on function public.assert_tournament_not_done(bigint, text) to authenticated;
grant execute on function public.assert_tournament_not_done(bigint, text) to service_role;

create or replace function public.guard_done_tournament_direct_write()
returns trigger
language plpgsql
as $$
declare
  v_tournament_id bigint;
begin
  v_tournament_id := case
    when tg_op = 'DELETE' then old.tournament_id
    else new.tournament_id
  end;

  perform public.assert_tournament_not_done(
    v_tournament_id,
    format('종료된 대회는 %s 테이블을 수정할 수 없습니다.', tg_table_name)
  );

  if tg_op = 'DELETE' then
    return old;
  end if;

  return new;
end;
$$;

