create table if not exists public.tournament_result_import_batches (
  id bigint generated by default as identity primary key,
  tournament_id bigint not null references public.tournaments(id) on delete cascade,
  source_kind text not null default 'manual'
    check (source_kind in ('manual', 'pdf_manual', 'pdf_ocr', 'external_import')),
  source_file_name text,
  source_checksum text,
  source_note text,
  imported_by uuid references auth.users(id),
  imported_at timestamptz not null default now(),
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.tournament_result_scorecards (
  id bigint generated by default as identity primary key,
  tournament_id bigint not null references public.tournaments(id) on delete cascade,
  import_batch_id bigint references public.tournament_result_import_batches(id) on delete set null,
  source_row_id bigint references public.tournament_result_rows(id) on delete set null,
  registration_id bigint references public.registrations(id) on delete set null,
  matched_user_id uuid references auth.users(id) on delete set null,
  section text not null default '단체 스코어',
  row_order int not null default 0,
  display_name text not null,
  source_display_name text,
  match_status text not null default 'pending'
    check (match_status in ('matched', 'ambiguous', 'pending')),
  verification_status text not null default 'draft'
    check (verification_status in ('draft', 'verified', 'needs_review')),
  verified_by uuid references auth.users(id),
  verified_at timestamptz,
  rank int,
  gross_total int,
  net_total int,
  handicap int,
  out_course text,
  in_course text,
  out_total int,
  in_total int,
  award text,
  near_value numeric(8, 2),
  long_value numeric(8, 2),
  stats jsonb not null default '{}'::jsonb,
  raw_payload jsonb not null default '{}'::jsonb,
  source_file_name text,
  source_page int,
  source_confidence numeric(5, 2),
  created_by uuid references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.tournament_result_scorecard_holes (
  id bigint generated by default as identity primary key,
  scorecard_id bigint not null references public.tournament_result_scorecards(id) on delete cascade,
  nine_type text not null check (nine_type in ('out', 'in')),
  hole_no int not null check (hole_no between 1 and 9),
  course_name text,
  par int,
  score int,
  result_label text,
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (scorecard_id, nine_type, hole_no)
);

